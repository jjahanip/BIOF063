# Integer
# An integer represents whole numbers without a decimal point.
my_integer = 42
print("Integer Example:")
print("Value:", my_integer)
print("Type:", type(my_integer))
print()

# Float
# A float represents numbers with a decimal point or in exponential form.
my_float = 3.14
print("Float Example:")
print("Value:", my_float)
print("Type:", type(my_float))
print()

# String
# A string represents a sequence of characters, such as text.
my_string = "Hello, Python!"
print("String Example:")
print("Value:", my_string)
print("Type:", type(my_string))
print()

# Boolean
# A boolean represents a binary value, either True or False, used for logical operations.
my_boolean = True
print("Boolean Example:")
print("Value:", my_boolean)
print("Type:", type(my_boolean))
print()

# List
# A list is an ordered collection of elements that can be of different data types.
my_list = [1, 2, 3, 4, 5]
print("List Example:")
print("Value:", my_list)
print("Type:", type(my_list))
print()

# Tuple
# A tuple is an ordered, immutable collection of elements.
my_tuple = (1, 2, 3, 4, 5)
print("Tuple Example:")
print("Value:", my_tuple)
print("Type:", type(my_tuple))
print()

# Dictionary
# A dictionary is an unordered collection of key-value pairs.
my_dict = {'name': 'John', 'age': 30}
print("Dictionary Example:")
print("Value:", my_dict)
print("Type:", type(my_dict))
print()

# Set
# A set is an unordered collection of unique elements.
my_set = {1, 2, 3, 4, 5}
print("Set Example:")
print("Value:", my_set)
print("Type:", type(my_set))
print()
