# Addition
result_addition = 5 + 3
print("Addition Example:")
print("Result:", result_addition)
print()

# Subtraction
result_subtraction = 10 - 4
print("Subtraction Example:")
print("Result:", result_subtraction)
print()

# Multiplication
result_multiplication = 6 * 7
print("Multiplication Example:")
print("Result:", result_multiplication)
print()

# Division
result_division = 15 / 3
print("Division Example:")
print("Result:", result_division)
print()

# Integer Division (Floor Division)
result_floor_division = 16 // 3
print("Floor Division Example:")
print("Result:", result_floor_division)
print()

# Modulus (Remainder)
result_modulus = 16 % 3
print("Modulus Example:")
print("Result:", result_modulus)
print()

# Exponentiation
result_exponentiation = 2 ** 4
print("Exponentiation Example:")
print("Result:", result_exponentiation)
print()

# Concatenation (for strings)
string1 = "Hello, "
string2 = "Python!"
result_concatenation = string1 + string2
print("String Concatenation Example:")
print("Result:", result_concatenation)
print()

# List Operations
list1 = [1, 2, 3]
list2 = [4, 5, 6]
result_list_concatenation = list1 + list2
print("List Concatenation Example:")
print("Result:", result_list_concatenation)
print()

# Comparison (Boolean)
comparison_result = 5 > 3
print("Comparison Example:")
print("Result:", comparison_result)
print()

# Logical Operations (Boolean)
logical_result = True and False
print("Logical AND Example:")
print("Result:", logical_result)
logical_result = True or False
print("Logical OR Example:")
print("Result:", logical_result)
logical_result = not True
print("Logical NOT Example:")
print("Result:", logical_result)
