# This is a comment in Python. Comments are not executed by the interpreter.
# They are used to provide explanations and make code more readable.

# Variable declaration and assignment
my_variable = 42  # This creates a variable named "my_variable" and assigns it the value 42.
my_string = "Hello, Python!"  # This creates a variable named "my_string" and assigns it a string value.

# Printing values
print("Value of my_variable:", my_variable)
print("Value of my_string:", my_string)

# Basic arithmetic operation
result = my_variable + 8  # Adding 8 to the value of my_variable
print("Result of addition:", result)

# Conditional statement
if result > 50:
    print("Result is greater than 50.")
else:
    print("Result is not greater than 50.")

# Looping example
for i in range(5):
    print("Iteration", i)

# Function definition
def greet(name):
    print("Hello,", name)

# Function call
greet("Alice")
